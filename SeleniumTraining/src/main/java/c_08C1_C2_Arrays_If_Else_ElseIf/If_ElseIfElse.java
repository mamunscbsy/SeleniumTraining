package c_08C1_C2_Arrays_If_Else_ElseIf;

public class If_ElseIfElse {

	public static void main(String[] args) {

		String name = "Mamun";

		if (name.equals("Mamun")) {
			System.out.println("This guy is awesome");
		} else if (name.equals("Samuel")) {
			System.out.println("This guy is ok I guess");
		} else {
			System.out.println("I don't know the guy at all");
		}
	}
}
